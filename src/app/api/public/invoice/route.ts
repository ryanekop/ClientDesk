export async function GET(request: NextRequest) {
  const code = request.nextUrl.searchParams.get("code");
  const lang = request.nextUrl.searchParams.get("lang") || "id";
  const stage = request.nextUrl.searchParams.get("stage") || "initial";
  const t = labels[lang] || labels.id;

  if (!code) {
    return NextResponse.json({ error: "Booking code required" }, { status: 400 });
  }

  const { data: booking, error } = await supabaseAdmin
    .from("bookings")
    .select(
      "id, booking_code, client_name, client_whatsapp, session_date, total_price, dp_paid, is_fully_paid, status, settlement_status, final_adjustments, final_payment_amount, final_paid_at, user_id, services(id, name, price, description, is_addon), booking_services(id, kind, sort_order, service:services(id, name, price, description, is_addon))",
    )
    .eq("booking_code", code)
    .single();

  if (!booking || error) {
    return NextResponse.json({ error: "Booking not found" }, { status: 404 });
  }

  const { data: profile } = await supabaseAdmin
    .from("profiles")
    .select("studio_name, invoice_logo_url")
    .eq("id", booking.user_id)
    .single();

  const studioName = profile?.studio_name || "Studio";
  const logoBase64 = profile?.invoice_logo_url || null;
  const adjustments = normalizeFinalAdjustments(booking.final_adjustments);
  const adjustmentsTotal = getFinalAdjustmentsTotal(adjustments);
  const finalInvoiceTotal = getFinalInvoiceTotal(booking.total_price, adjustments);
  const isFinalStage = stage === "final";
  const invoiceTitle = isFinalStage ? t.finalInvoice : t.invoice;
  const shownTotal = isFinalStage ? finalInvoiceTotal : booking.total_price;
  const remaining = isFinalStage
    ? getRemainingFinalPayment({
        total_price: booking.total_price,
        dp_paid: booking.dp_paid,
        final_adjustments: adjustments,
        final_payment_amount: booking.final_payment_amount,
        final_paid_at: booking.final_paid_at,
        settlement_status: booking.settlement_status,
        is_fully_paid: booking.is_fully_paid,
      })
    : Math.max(booking.total_price - booking.dp_paid, 0);
  const dateLocale = lang === "en" ? "en-US" : "id-ID";
  const sessionDate = booking.session_date
    ? formatSessionDate(booking.session_date, {
        locale: lang === "en" ? "en" : "id",
        dateOnly: true,
      })
    : "-";
  const now = new Date().toLocaleDateString(dateLocale, {
    day: "numeric",
    month: "long",
    year: "numeric",
  });
  const legacyService = normalizeLegacyServiceRecord(booking.services);
  const serviceSelections = normalizeBookingServiceSelections(
    (booking as { booking_services?: unknown[] }).booking_services,
    booking.services,
  );
  const serviceName = getBookingServiceLabel(serviceSelections, {
    kind: "main",
    fallback: legacyService?.name || t.defaultServiceName,
  });
  const mainServices = serviceSelections.filter((selection) => selection.kind === "main");
  const mainServiceDescriptions = mainServices
    .filter((selection) => Boolean(selection.service.description?.trim()))
    .map((selection) => {
      const description = selection.service.description?.trim() || "";
      if (mainServices.length === 1) return description;
      return `${selection.service.name}: ${description}`;
    });
  const serviceDescription =
    mainServiceDescriptions.join("\n\n") || legacyService?.description?.trim() || "";

  const settlementStatus = getSettlementStatus(booking.settlement_status);
  const paymentStatus =
    isFinalStage && settlementStatus === "submitted"
      ? t.pendingVerification
      : booking.is_fully_paid
        ? t.paid
        : t.unpaid;

  const pdfDoc = await PDFDocument.create();
  const helvetica = await pdfDoc.embedFont(StandardFonts.Helvetica);
  const helveticaBold = await pdfDoc.embedFont(StandardFonts.HelveticaBold);

  const black = rgb(0.1, 0.1, 0.1);
  const gray = rgb(0.42, 0.44, 0.47);
  const lightGray = rgb(0.9, 0.91, 0.92);
  const green = rgb(0.086, 0.64, 0.26);
  const amber = rgb(0.85, 0.47, 0.02);
  const blue = rgb(0.11, 0.38, 0.87);

  const PAGE_WIDTH = 595;
  const PAGE_HEIGHT = 842;
  const MARGIN_X = 40;
  const TOP_Y = 802;
  const BOTTOM_Y = 50;
  const contentW = PAGE_WIDTH - MARGIN_X * 2;

  let page: PDFPage = pdfDoc.addPage([PAGE_WIDTH, PAGE_HEIGHT]);
  let y = TOP_Y;

  const addPage = () => {
    page = pdfDoc.addPage([PAGE_WIDTH, PAGE_HEIGHT]);
    y = TOP_Y;
  };

  const ensureSpace = (requiredHeight: number) => {
    if (y - requiredHeight < BOTTOM_Y) {
      addPage();
    }
  };

  const drawHorizontalLine = (
    startX: number,
    endX: number,
    thickness: number,
    color = lightGray,
  ) => {
    page.drawLine({
      start: { x: startX, y },
      end: { x: endX, y },
      thickness,
      color,
    });
  };

  const drawWrappedText = (params: {
    text: string;
    x: number;
    maxWidth: number;
    font: PDFFont;
    size: number;
    color: ReturnType<typeof rgb>;
    lineHeight: number;
  }) => {
    const lines = wrapTextLines(
      params.text,
      params.font,
      params.size,
      params.maxWidth,
    );

    if (lines.length === 0) {
      ensureSpace(params.lineHeight);
      page.drawText(t.noDescription, {
        x: params.x,
        y,
        font: params.font,
        size: params.size,
        color: params.color,
      });
      y -= params.lineHeight;
      return;
    }

    lines.forEach((line) => {
      ensureSpace(params.lineHeight);
      if (line) {
        page.drawText(line, {
          x: params.x,
          y,
          font: params.font,
          size: params.size,
          color: params.color,
        });
      }
      y -= params.lineHeight;
    });
  };

  let logoDrawn = false;
  if (logoBase64) {
    try {
      const base64Data = logoBase64.replace(/^data:image\/(png|jpe?g);base64,/, "");
      const imageBytes = Buffer.from(base64Data, "base64");
      let image;
      if (logoBase64.includes("image/png")) {
        image = await pdfDoc.embedPng(imageBytes);
      } else {
        image = await pdfDoc.embedJpg(imageBytes);
      }
      const maxW = 148;
      const maxH = 52;
      const scale = Math.min(maxW / image.width, maxH / image.height, 1);
      const drawW = image.width * scale;
      const drawH = image.height * scale;
      page.drawImage(image, {
        x: MARGIN_X,
        y: y - drawH + 22,
        width: drawW,
        height: drawH,
      });
      logoDrawn = true;
    } catch {
      logoDrawn = false;
    }
  }

  if (!logoDrawn) {
    page.drawText(studioName, {
      x: MARGIN_X,
      y,
      font: helveticaBold,
      size: 18,
      color: black,
    });
  }
  page.drawText(invoiceTitle, {
    x: PAGE_WIDTH - MARGIN_X - helveticaBold.widthOfTextAtSize(invoiceTitle, 22),
    y,
    font: helveticaBold,
    size: 22,
    color: black,
  });

  y -= 18;
  if (!logoDrawn) {
    page.drawText(t.studioManagement, {
      x: MARGIN_X,
      y,
      font: helvetica,
      size: 9,
      color: gray,
    });
  }

  const codeW = helvetica.widthOfTextAtSize(booking.booking_code, 10);
  page.drawText(booking.booking_code, {
    x: PAGE_WIDTH - MARGIN_X - codeW,
    y,
    font: helvetica,
    size: 10,
    color: gray,
  });

  y -= 14;
  const dateW = helvetica.widthOfTextAtSize(now, 9);
  page.drawText(now, {
    x: PAGE_WIDTH - MARGIN_X - dateW,
    y,
    font: helvetica,
    size: 9,
    color: gray,
  });

  y -= 16;
  drawHorizontalLine(MARGIN_X, PAGE_WIDTH - MARGIN_X, 1);

  y -= 24;
  page.drawText(t.clientDetail, {
    x: MARGIN_X,
    y,
    font: helveticaBold,
    size: 8,
    color: gray,
  });

  y -= 18;
  page.drawText(booking.client_name, {
    x: MARGIN_X,
    y,
    font: helveticaBold,
    size: 12,
    color: black,
  });

  y -= 16;
  page.drawText(booking.client_whatsapp || "-", {
    x: MARGIN_X,
    y,
    font: helvetica,
    size: 10,
    color: gray,
  });

  y -= 30;
  const colX = [MARGIN_X, MARGIN_X + 200, MARGIN_X + 320, PAGE_WIDTH - MARGIN_X];
  const headers = [t.service, t.schedule, t.status, t.total];
  page.drawRectangle({
    x: MARGIN_X,
    y: y - 4,
    width: contentW,
    height: 28,
    color: rgb(0.976, 0.98, 0.984),
  });

  headers.forEach((header, index) => {
    const tx =
      index === 3
        ? colX[index] - helveticaBold.widthOfTextAtSize(header, 8)
        : colX[index] + 8;
    page.drawText(header, {
      x: tx,
      y: y + 6,
      font: helveticaBold,
      size: 8,
      color: gray,
    });
  });

  y -= 6;
  drawHorizontalLine(MARGIN_X, PAGE_WIDTH - MARGIN_X, 0.5);

  y -= 20;
  page.drawText(serviceName, {
    x: colX[0] + 8,
    y,
    font: helvetica,
    size: 10,
    color: black,
  });
  page.drawText(sessionDate, {
    x: colX[1] + 8,
    y,
    font: helvetica,
    size: 10,
    color: black,
  });

  const statusColor =
    paymentStatus === t.paid
      ? green
      : paymentStatus === t.pendingVerification
        ? blue
        : amber;
  page.drawText(paymentStatus, {
    x: colX[2] + 8,
    y,
    font: helveticaBold,
    size: 10,
    color: statusColor,
  });

  const totalStr = formatCurrency(shownTotal);
  page.drawText(totalStr, {
    x: colX[3] - helvetica.widthOfTextAtSize(totalStr, 10),
    y,
    font: helvetica,
    size: 10,
    color: black,
  });

  y -= 14;
  drawHorizontalLine(MARGIN_X, PAGE_WIDTH - MARGIN_X, 0.5, rgb(0.95, 0.96, 0.96));

  y -= 20;
  ensureSpace(32);
  page.drawText(t.packageDescription, {
    x: MARGIN_X,
    y,
    font: helveticaBold,
    size: 10,
    color: black,
  });

  y -= 14;
  if (serviceDescription.trim()) {
    drawWrappedText({
      text: serviceDescription,
      x: MARGIN_X + 8,
      maxWidth: contentW - 16,
      font: helvetica,
      size: 9,
      color: gray,
      lineHeight: 12,
    });
  } else {
    ensureSpace(12);
    page.drawText(t.noDescription, {
      x: MARGIN_X + 8,
      y,
      font: helvetica,
      size: 9,
      color: gray,
    });
    y -= 12;
  }

  y -= 8;
  drawHorizontalLine(MARGIN_X, PAGE_WIDTH - MARGIN_X, 0.5, rgb(0.95, 0.96, 0.96));

  if (isFinalStage) {
    y -= 22;
    ensureSpace(28);
    page.drawText(t.adjustments, {
      x: MARGIN_X,
      y,
      font: helveticaBold,
      size: 10,
      color: black,
    });
    y -= 14;

    if (adjustments.length === 0) {
      ensureSpace(12);
      page.drawText("-", {
        x: MARGIN_X,
        y,
        font: helvetica,
        size: 10,
        color: gray,
      });
      y -= 12;
    } else {
      adjustments.forEach((item) => {
        ensureSpace(24);
        page.drawText(item.label, {
          x: MARGIN_X,
          y,
          font: helveticaBold,
          size: 10,
          color: black,
        });

        const amountText = formatCurrency(item.amount);
        page.drawText(amountText, {
          x: PAGE_WIDTH - MARGIN_X - helveticaBold.widthOfTextAtSize(amountText, 10),
          y,
          font: helveticaBold,
          size: 10,
          color: black,
        });

        y -= 14;
        const reason = item.reason || t.noReason;
        drawWrappedText({
          text: reason,
          x: MARGIN_X + 8,
          maxWidth: contentW - 16,
          font: helvetica,
          size: 9,
          color: gray,
          lineHeight: 12,
        });
        y -= 4;
      });
    }
  }

  y -= 14;
  ensureSpace(isFinalStage ? 120 : 96);

  const sumX = PAGE_WIDTH - MARGIN_X - 220;
  const sumValX = PAGE_WIDTH - MARGIN_X;

  page.drawText(t.subTotal, {
    x: sumX,
    y,
    font: helvetica,
    size: 10,
    color: black,
  });
  const subTotalText = formatCurrency(booking.total_price);
  page.drawText(subTotalText, {
    x: sumValX - helvetica.widthOfTextAtSize(subTotalText, 10),
    y,
    font: helvetica,
    size: 10,
    color: black,
  });

  if (isFinalStage) {
    y -= 22;
    page.drawText(t.adjustmentTotal, {
      x: sumX,
      y,
      font: helvetica,
      size: 10,
      color: black,
    });
    const adjText = formatCurrency(adjustmentsTotal);
    page.drawText(adjText, {
      x: sumValX - helvetica.widthOfTextAtSize(adjText, 10),
      y,
      font: helvetica,
      size: 10,
      color: black,
    });
  }

  y -= 22;
  page.drawText(t.dpPaid, {
    x: sumX,
    y,
    font: helvetica,
    size: 10,
    color: black,
  });
  const dpText = `- ${formatCurrency(booking.dp_paid)}`;
  page.drawText(dpText, {
    x: sumValX - helvetica.widthOfTextAtSize(dpText, 10),
    y,
    font: helvetica,
    size: 10,
    color: black,
  });

  y -= 14;
  drawHorizontalLine(sumX, sumValX, 1.5, black);

  y -= 20;
  page.drawText(t.remainingPayment, {
    x: sumX,
    y,
    font: helveticaBold,
    size: 13,
    color: black,
  });
  const remainingText = formatCurrency(remaining);
  page.drawText(remainingText, {
    x: sumValX - helveticaBold.widthOfTextAtSize(remainingText, 13),
    y,
    font: helveticaBold,
    size: 13,
    color: black,
  });

  y -= 50;
  ensureSpace(30);
  drawHorizontalLine(MARGIN_X, PAGE_WIDTH - MARGIN_X, 0.5);

  y -= 16;
  const footerText = t.footerText;
  const footerWidth = helvetica.widthOfTextAtSize(footerText, 9);
  page.drawText(footerText, {
    x: (PAGE_WIDTH - footerWidth) / 2,
    y,
    font: helvetica,
    size: 9,
    color: gray,
  });

  const pdfBytes = await pdfDoc.save();
  const filename = `${isFinalStage ? "Final-Invoice" : "Invoice"}-${booking.booking_code}.pdf`;

  return new NextResponse(Buffer.from(pdfBytes), {
    headers: {
      "Content-Type": "application/pdf",
      "Content-Disposition": `inline; filename="${filename}"`,
    },
  });
}
